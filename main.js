function smash (words) {
  return words.join(' ')
};

console.log(smash(['hello', 'general', 'kenobi']))

// parameters: words     an array of words
// return: sentence    a string made up of the words in the param array
// e.g. smash(['hello', 'general', 'kenobi'])
// results in 'hello general kenobi'


